# Learning about numeric systems on python
# Decimal
a = 10
print(f'a: {a}')
# Bin
a = 0b1010
print(f'a: {a}')
# Octal
a = 0o12
print(f'a: {a}')
# Hexadecimal
a = 0xA
print(f'a: {a}')


# Convertir un ints, incluyendo al base
a =int('23', 10)# Base10
a = int('10111', 2)# Bin
a = int('27', 8)#Octal
a = int('17', 16)#Hexadecimal

print(f'a: {a}')